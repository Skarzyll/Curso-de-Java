package project11;

public class Caneta {
    //atributos
    private String  modelo;
    private float   ponta;
    private String  cor;
    private boolean tampada;

    //metodo construtor
    public Caneta() {
        this.cor = "azul";
        this.tampada = true;
    }

    //metodos especiais
    private String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public float getPonta() {
        return ponta;
    }

    public void setPonta(float ponta) {
        this.ponta = ponta;
    }

    public String getCor() {
        return cor;
    }

    public void tampar() {
        this.tampada = true;
    }

    public void destampar() {
        this.tampada = false;
    }

    public void status() {
        System.out.println("SOBRE A CANETA:");
        System.out.println("Modelo: " + this.getModelo());
        System.out.println("Ponta: " + this.getPonta());
        System.out.println("Cor: " + this.cor);
        System.out.println("Tampada: " + (this.tampada == true ? "Sim" : "NÃ£o"));
    }
}
